# ======================================
# STRING FORMATTING IN PYTHON
# ======================================

# --------------------------------------
# 1. Formatting using format() with names
# --------------------------------------

string1 = "Name: {name} Age: {age}"
print(string1.format(name="Prawin", age=22))

# Output:
# Name: Prawin Age: 22


# --------------------------------------
# 2. Positional Formatting
# --------------------------------------

print("Name: {1} Age: {0}".format(22, "Prawin"))

# Output:
# Name: Prawin Age: 22


# --------------------------------------
# 3. Binary Representation
# --------------------------------------

print(f"{10:b}")

# Output:
# 1010


# --------------------------------------
# 4. f-String Variables
# --------------------------------------

name = "Prawin"
age = 22

print(f"Name: {name} Age: {age}")

# Output:
# Name: Prawin Age: 22


# String concatenation (Alternative)
# print("Name: " + name + " Age: " + str(age))


# ======================================
# NUMBER FORMATTING
# ======================================

# --------------------------------------
# 5. Decimal Precision
# --------------------------------------

print(f"{3.234543:.3f}")

# Output:
# 3.235


# --------------------------------------
# 6. Width Formatting
# --------------------------------------

print(f"{20:9}")
print(f"{20:8}")

# Output:
#        20
#       20


# --------------------------------------
# 7. Zero Padding
# --------------------------------------

print(f"{20: 05}")
print(f"{20:05}")
print(f"{20:09}")

# Output:
#  0020
# 00020
# 000000020


# --------------------------------------
# 8. Comma Separator
# --------------------------------------

print(f"{20000:,}")

# Output:
# 20,000


# --------------------------------------
# 9. Percentage Formatting
# --------------------------------------

print(f"{0.80:.2%}")

# Output:
# 80.00%


# ======================================
# ALIGNMENT
# ======================================

# --------------------------------------
# 10. Right, Left, Center Alignment
# --------------------------------------

print(f"-{20:>5}-")   # Right
print(f"-{20:<5}-")   # Left
print(f"-{20:^5}-")   # Center

# Output:
# -   20-
# -20   -
# - 20  -


# --------------------------------------
# 11. Alignment with Zero Fill
# --------------------------------------

print(f"-{20:>06}-")
print(f"-{20:<06}-")
print(f"-{20:^06}-")

# Output:
# -000020-
# -200000-
# -002000-